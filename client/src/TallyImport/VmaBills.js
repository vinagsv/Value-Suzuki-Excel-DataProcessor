/**
 * Converts the "BC AND VMA BILLS" sheet in an Excel file into Tally import XML.
 *
 * @param {File}    file          - The uploaded .xlsx / .xls file
 * @param {string}  [fromDateStr] - Optional filter start date in YYYYMMDD format
 * @param {string}  [toDateStr]   - Optional filter end date in YYYYMMDD format
 * @returns {{ xmlData: string, createdCount: number, cancelledCount: number, skippedCount: number }}
 */
export async function processVMAFileClientSide(file, fromDateStr = null, toDateStr = null) {
    if (!file) throw new Error("No file provided for processing.");

    const arrayBuffer = await file.arrayBuffer();
    const workbook = xlsx.read(arrayBuffer, { type: 'array' });

    if (!workbook.SheetNames.includes(CONFIG.TARGET_SHEET_NAME)) {
        throw new Error(
            `Sheet named "${CONFIG.TARGET_SHEET_NAME}" not found in the uploaded Excel file.`
        );
    }

    const sheet = workbook.Sheets[CONFIG.TARGET_SHEET_NAME];
    const data  = xlsx.utils.sheet_to_json(sheet, { header: 1 });

    if (data.length === 0) throw new Error("Excel sheet is empty.");

    // Normalise header row
    const headers = data[0].map(h => (h || '').toString().trim().toUpperCase());

    const dateIdx    = headers.indexOf(CONFIG.EXCEL_MAPPING.DATE.toUpperCase());
    const billNumIdx = headers.indexOf(CONFIG.EXCEL_MAPPING.BILL_NUM.toUpperCase());

    if (dateIdx === -1 || billNumIdx === -1) {
        throw new Error(
            `Required column "${CONFIG.EXCEL_MAPPING.DATE}" or ` +
            `"${CONFIG.EXCEL_MAPPING.BILL_NUM}" not found in the sheet header.`
        );
    }

    const colIdx = {
        date:      dateIdx,
        billNum:   billNumIdx,
        mode:      headers.indexOf(CONFIG.EXCEL_MAPPING.PAYMENT_MODE.toUpperCase()),
        amount:    headers.indexOf(CONFIG.EXCEL_MAPPING.AMOUNT.toUpperCase()),
        narration: headers.indexOf(CONFIG.EXCEL_MAPPING.NARRATION.toUpperCase())
    };

    let xml            = buildEnvelopeHeader();
    let createdCount   = 0;
    let cancelledCount = 0;
    let skippedCount   = 0;
    let lastValidSeq   = null;   // tracks the last processed bill sequence number

    for (let i = 1; i < data.length; i++) {
        const row = data[i];
        if (!row || row.length === 0) { skippedCount++; continue; }

        // ── Validate bill number ──
        const rawBillNumber = (row[colIdx.billNum] || '').toString().trim();
        if (!rawBillNumber || !CONFIG.BILL_FORMAT.REGEX.test(rawBillNumber)) {
            skippedCount++;
            continue;
        }

        const billMatch  = rawBillNumber.match(CONFIG.BILL_FORMAT.REGEX);
        const currentSeq = parseInt(billMatch[1], 10);
        // Canonical form: always VMA/XXXXXX
        const billNumber = `${CONFIG.BILL_FORMAT.PREFIX}/${billMatch[1]}`;

        // ── Parse date ──
        const dateFormatted = parseDateCell(row[colIdx.date]);
        if (!dateFormatted) { skippedCount++; continue; }

        // ── Date-range filter ──
        if (fromDateStr && dateFormatted < fromDateStr) { skippedCount++; continue; }
        if (toDateStr   && dateFormatted > toDateStr)   { skippedCount++; continue; }

        // ── Fill in cancelled stubs for any sequence gaps ──
        if (lastValidSeq !== null && currentSeq > lastValidSeq + 1) {
            for (let missingSeq = lastValidSeq + 1; missingSeq < currentSeq; missingSeq++) {
                const missingBill = `${CONFIG.BILL_FORMAT.PREFIX}/${missingSeq}`;
                xml += buildCancelledXML(
                    dateFormatted,
                    missingBill,
                    'Auto imported from Excel, cancelled due to missing bill number in sequence'
                );
                cancelledCount++;
            }
        }
        lastValidSeq = currentSeq;

        // ── Read remaining fields ──
        const rawPaymentMode = colIdx.mode !== -1
            ? (row[colIdx.mode] || '').toString().trim()
            : '';
        const rawAmount = colIdx.amount !== -1 ? row[colIdx.amount] : '';
        const rawNarration = colIdx.narration !== -1
            ? (row[colIdx.narration] || '').toString().trim()
            : '';

        const validLedger      = getValidLedgerName(rawPaymentMode);
        const amountIsInvalid  = isInvalidNumber(rawAmount);
        const targetTotal      = amountIsInvalid ? 0 : (parseFloat(rawAmount) || 0);

        /**
         * Builds the narration string for a cancelled voucher.
         * Always includes the cancellation reason; appends the Excel narration
         * (from the VMA NARRATION column) when one is present, separated by " | ".
         */
        const cancelNarration = (reason) =>
            rawNarration
                ? `Auto imported from Excel, cancelled due to ${reason} | ${rawNarration}`
                : `Auto imported from Excel, cancelled due to ${reason}`;

        if (!validLedger) {
            // Unknown / missing payment mode
            const modeDetail = rawPaymentMode ? `: "${rawPaymentMode}"` : '';
            xml += buildCancelledXML(
                dateFormatted,
                billNumber,
                cancelNarration(`invalid or missing payment mode${modeDetail}`)
            );
            cancelledCount++;
        } else if (amountIsInvalid || targetTotal <= 0) {
            // Missing or zero amount
            xml += buildCancelledXML(
                dateFormatted,
                billNumber,
                cancelNarration('invalid or zero amount')
            );
            cancelledCount++;
        } else {
            xml += buildNormalXML(
                dateFormatted,
                billNumber,
                validLedger,
                targetTotal,
                rawNarration   // raw narration from Excel; empty string if none
            );
            createdCount++;
        }
    }

    xml += buildEnvelopeFooter();

    return { xmlData: xml, createdCount, cancelledCount, skippedCount };
}