const isInvalidNumber = (val) => {
    if (val === undefined || val === null) return true;
    const strVal = val.toString().trim();
    if (strVal === '') return true;
    if (isNaN(Number(strVal))) return true;
    return false;
};

const getValidLedgerName = (mode) => {
    const trimmedMode = (mode || '').toString().trim();
    const match = CONFIG.LEDGERS.ALLOWED_PAYMENT_MODES.find(
        l => l.toLowerCase() === trimmedMode.toLowerCase()
    );
    return match || null;
};

/**
 * Returns true if the payment mode is a cash-type ledger
 * (unregistered/consumer walk-in, no GST number).
 * Affects VATDEALERTYPE, ISCOMMONPARTY, HASCASHFLOW in the generated XML.
 */
const isCashTypeLedger = (ledgerName) => {
    return CONFIG.LEDGERS.CASH_TYPE_LEDGERS.some(
        l => l.toLowerCase() === ledgerName.toLowerCase()
    );
};

/**
 * Normalises the bill number to the canonical VMA/XXXXXX format Tally expects.
 * Input can be "VMA260867" or "VMA/260867" — output is always "VMA/260867".
 */
const normaliseBillNumber = (rawBill) => {
    const match = rawBill.match(CONFIG.BILL_FORMAT.REGEX);
    if (!match) return null;
    return `${CONFIG.BILL_FORMAT.PREFIX}/${match[1]}`;
};

/**
 * Escape characters that are invalid inside XML text nodes.
 */
const escapeXml = (str) => {
    return (str || '').toString()
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')
        .replace(/"/g, '&quot;')
        .replace(/'/g, '&apos;');
};

/**
 * Parse a date cell from xlsx into a YYYYMMDD string.
 * Returns empty string on failure.
 */
const parseDateCell = (dateRaw) => {
    if (dateRaw === undefined || dateRaw === null) return '';

    if (typeof dateRaw === 'number') {
        const parsed = xlsx.SSF.parse_date_code(dateRaw);
        const yyyy = parsed.y;
        const mm = String(parsed.m).padStart(2, '0');
        const dd = String(parsed.d).padStart(2, '0');
        return `${yyyy}${mm}${dd}`;
    }

    if (dateRaw instanceof Date) {
        const yyyy = dateRaw.getUTCFullYear();
        const mm = String(dateRaw.getUTCMonth() + 1).padStart(2, '0');
        const dd = String(dateRaw.getUTCDate()).padStart(2, '0');
        return `${yyyy}${mm}${dd}`;
    }

    if (typeof dateRaw === 'string') {
        const dStr = dateRaw.trim();
        // YYYY-MM-DD or YYYY/MM/DD
        if (/^\d{4}[-/]\d{2}[-/]\d{2}$/.test(dStr)) {
            return dStr.replace(/[-/]/g, '');
        }
        // DD-MM-YYYY or DD/MM/YYYY
        if (/^\d{2}[-/]\d{2}[-/]\d{4}$/.test(dStr)) {
            const parts = dStr.split(/[-/]/);
            return `${parts[2]}${parts[1]}${parts[0]}`;
        }
        // Fallback: attempt JS Date parse (treats as UTC-neutral)
        const temp = new Date(dStr);
        if (!isNaN(temp)) {
            const yyyy = temp.getUTCFullYear();
            const mm = String(temp.getUTCMonth() + 1).padStart(2, '0');
            const dd = String(temp.getUTCDate()).padStart(2, '0');
            return `${yyyy}${mm}${dd}`;
        }
    }

    return '';
};

